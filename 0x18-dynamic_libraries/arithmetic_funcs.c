#include <stdio.h>

/**
 * add - add two numbers
 * @a: first number
 * @b: second number
 *
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - difference of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 *
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide a number by the other
 * @a: first number
 * @b: second number
 *
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of the division of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
